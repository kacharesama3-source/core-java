package Datatypes;

public class Shorthand {

	public static void main(String[] args) {
		//it is simple and quick for using without changing meaning ,its arithmetic operation 
		int num =26;
		num+=10;// num=num+10 
		num+=20;
		
		System.out.println(num-2);//56-2=54
		
		System.out.println(num);//56
		System.out.println(num*4);//56*4
		System.out.println(56*4);
	
		
		
		
		}

}
