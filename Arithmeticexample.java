package Datatypes;

public class Arithmeticexample {

	public static void main(String[] args) {
		int a = 200;
		int b = 56;
		System.out.println("a+b = " + (a+b));
		System.out.println("a-b = " + (a-b)); // +  symbol  for string concatenation
        System.out.println("a*b =" + (a*b));
        System.out.println("a/b ="+(a/b));
        System.out.println("a%b="+(a%b));
     ++a;// 201
     --b;
     a++;//201=>202
     ++b;
     System.out.println(a+20);
     System.out.println(b);
        
        
	}

}
