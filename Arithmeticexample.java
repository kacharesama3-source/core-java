package Datatypes;

public class Arithmeticexample {

	public static void main(String[] args) {
		int a = 20;
		int b = 56;
		System.out.println("a+b = " + (a+b));
		System.out.println("a-b = " + (a-b)); // +  symbol  for string concatenation
        System.out.println("a*b =" + (a*b));
        System.out.println("a/b ="+(a/b));
        
	}

}
